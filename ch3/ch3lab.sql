-- between 500-800 on the HDD
-- resolution 1024x768 or 1280x1024;

DESCRIBE product_information;
       
SELECT PRODUCT_NAME, LIST_PRICE
FROM PRODUCT_INFORMATION
WHERE PRODUCT_NAME LIKE 'HD%' AND LIST_PRICE BETWEEN 500 AND 800 
   OR PRODUCT_NAME LIKE 'GP%' AND 
      (PRODUCT_NAME LIKE '%1024x768%' OR
       PRODUCT_NAME LIKE '%1280x1024%');